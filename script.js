function resizeIntroCanvas() {
  const dpr = Math.min(window.devicePixelRatio || 1, 2);

  introCanvas.width = window.innerWidth * dpr;
  introCanvas.height = window.innerHeight * dpr;
  introCanvas.style.width = window.innerWidth + "px";
  introCanvas.style.height = window.innerHeight + "px";

  introCtx.setTransform(dpr, 0, 0, dpr, 0, 0);
}

function seedIntroParticles() {
  introParticles = Array.from({ length: 78 }, (_, i) => ({
    angle: ((Math.PI * 2) / 78) * i,
    radius: 120 + Math.random() * 250,
    size: 1 + Math.random() * 2.5,
    speed: 0.002 + Math.random() * 0.005,
    drift: Math.random() * Math.PI * 2,
    color:
      Math.random() > 0.5
        ? "103,232,249"
        : Math.random() > 0.5
          ? "59,130,246"
          : "139,92,246"
  }));
}

function animateIntroParticles() {
  introCtx.clearRect(0, 0, window.innerWidth, window.innerHeight);

  const centerX = window.innerWidth * 0.71;
  const centerY = window.innerHeight * 0.52;
  const pullX = (mouse.x - centerX) * 0.18;
  const pullY = (mouse.y - centerY) * 0.18;

  introParticles.forEach((p, index) => {
    p.angle += p.speed;
    p.drift += 0.01;

    const wave = Math.sin(p.drift) * 22;
    const x = centerX + pullX + Math.cos(p.angle) * (p.radius + wave);
    const y = centerY + pullY + Math.sin(p.angle * 0.78) * (p.radius * 0.46 + wave);
    const alpha = 0.22 + Math.sin(p.drift + index) * 0.18;

    introCtx.beginPath();
    introCtx.arc(x, y, p.size, 0, Math.PI * 2);
    introCtx.fillStyle = `rgba(${p.color}, ${alpha})`;
    introCtx.shadowBlur = 18;
    introCtx.shadowColor = `rgba(${p.color}, 0.85)`;
    introCtx.fill();

    if (index % 3 === 0) {
      introCtx.beginPath();
      introCtx.moveTo(x, y);
      introCtx.lineTo(
        x - Math.cos(p.angle) * 42,
        y - Math.sin(p.angle) * 22
      );
      introCtx.strokeStyle = `rgba(${p.color}, 0.12)`;
      introCtx.lineWidth = 1;
      introCtx.stroke();
    }
  });

  introCtx.shadowBlur = 0;
  requestAnimationFrame(animateIntroParticles);
}

window.addEventListener("resize", resizeIntroCanvas);


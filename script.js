function setLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");

    if (Object.prototype.hasOwnProperty.call(translations[language], key)) {
      element.innerHTML = translations[language][key];
    }
  });

  const nextLanguageLabel = language === "en" ? "TR" : "EN";

  [languageToggle, mobileLanguageToggle].forEach((button) => {
    if (!button) return;

    button.textContent = nextLanguageLabel;
    button.setAttribute(
      "aria-label",
      language === "en" ? "Switch to Turkish" : "İngilizceye geç"
    );
  });
}

function toggleLanguage() {
  setLanguage(currentLanguage === "en" ? "tr" : "en");
}

languageToggle?.addEventListener("click", toggleLanguage);
mobileLanguageToggle?.addEventListener("click", toggleLanguage);
setLanguage("en");


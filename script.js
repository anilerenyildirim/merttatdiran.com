function initImageGallery({ trackId, prevId, nextId, dotsId, counterId, dotLabel }) {
  const track = document.getElementById(trackId);
  const prev = document.getElementById(prevId);
  const next = document.getElementById(nextId);
  const dots = document.getElementById(dotsId);
  const counter = document.getElementById(counterId);
  const slides = track ? Array.from(track.querySelectorAll(".image-slide")) : [];
  let currentIndex = 0;

  function updateGallery() {
    if (!track || slides.length === 0) return;

    track.style.transform = `translateX(-${currentIndex * 100}%)`;

    if (counter) {
      counter.textContent = `${currentIndex + 1} / ${slides.length}`;
    }

    if (dots) {
      dots.querySelectorAll("button").forEach((dot, index) => {
        dot.classList.toggle("active", index === currentIndex);
        dot.setAttribute("aria-current", index === currentIndex ? "true" : "false");
      });
    }
  }

  function goToSlide(index) {
    if (slides.length === 0) return;
    currentIndex = (index + slides.length) % slides.length;
    updateGallery();
  }

  if (dots && slides.length > 0) {
    slides.forEach((_, index) => {
      const dot = document.createElement("button");
      dot.type = "button";
      dot.setAttribute("aria-label", `${dotLabel} ${index + 1}`);
      dot.addEventListener("click", () => goToSlide(index));
      dots.appendChild(dot);
    });
  }

  prev?.addEventListener("click", () => goToSlide(currentIndex - 1));
  next?.addEventListener("click", () => goToSlide(currentIndex + 1));

  updateGallery();
}

